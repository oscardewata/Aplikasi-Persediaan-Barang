    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
        if (!no_faktur.getText().equals("") && !cmbkodesupplier.getSelectedItem().equals("") && !nama_supplier.getText().equals("") && !cmbkodebarang.getSelectedItem().equals("") && !nama_barang.getText().equals("") && !jumlah.getText().equals("")){
        try {
            Connection koneksi = DriverManager.getConnection("jdbc:mysql://localhost:3306/persediaan_barang","root","");
            koneksi.createStatement().executeUpdate("INSERT INTO tbl_transaksi_pembelian (no_faktur, nama_supplier, nama_barang, jumlah, tanggal) VALUES ('"+no_faktur.getText()+"','"+nama_supplier.getText()+"','"+nama_barang.getText()+"','"+jumlah.getText()+"','"+tanggal.getText()+"')");
            tampilkan();
            reset();
            JOptionPane.showMessageDialog(null, "Input Data Transaksi Pembelian Berhasil");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Nomor Faktur Sudah Ada");
//            Logger.getLogger(transaksi_pembelian.class.getName()).log(Level.SEVERE, null, ex);
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "Simpan Gagal");
        }
        }
    }//GEN-LAST:event_btnsimpanActionPerformed
  
