    private void tampilkan() {
        try {
            int row = tbltransaksi.getRowCount();
            for (int i = 0; i < row; i++) {
                model.removeRow(0);
            }
            Connection koneksi = DriverManager.getConnection("jdbc:mysql://localhost:3306/persediaan_barang", "root", "");
            ResultSet rs = koneksi.createStatement().executeQuery("SELECT no_faktur, nama_supplier, nama_barang, jumlah, tanggal FROM tbl_transaksi_pembelian");
            while (rs.next()){
                String [] data = {rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5)};
                model.addRow(data);
            }   } catch (SQLException ex) {
            Logger.getLogger(transaksi_pembelian.class.getName()).log(Level.SEVERE, null, ex);
        }
}
