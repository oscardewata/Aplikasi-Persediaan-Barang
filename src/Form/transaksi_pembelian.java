    public void tampil_combo_kode_supplier()
    {
        try {
        Connection con = koneksi.getKoneksi();
        Statement stt = con.createStatement();
        String sql = "select kode_supplier from tbl_supplier order by kode_supplier asc";      
        ResultSet res = stt.executeQuery(sql);                               
        cmbkodesupplier.addItem("==Pilih==");
        while(res.next()){
            Object[] ob = new Object[2];
            ob[0] = res.getString(1);
            
            cmbkodesupplier.addItem((String) ob[0]);                                      
        }
        res.close(); stt.close();
         
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
    
