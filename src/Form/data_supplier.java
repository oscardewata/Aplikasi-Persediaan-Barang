    private void btncariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncariActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Kode Supplier");
        model.addColumn("Nama Supplier");
        model.addColumn("Alamat Supplier");
        model.addColumn("No. Telp");
        //menampilkan data database kedalam tabel
        try {

            String sql = "select * from tbl_supplier where kode_supplier LIKE '%"+txtcari.getText()+"%'"
            + "OR nama_supplier LIKE '%"+txtcari.getText()+"%' OR alamat_supplier LIKE '%"+txtcari.getText()+"%'"
            + "OR no_telp LIKE '%"+txtcari.getText()+"%' ORDER BY kode_supplier";
            java.sql.Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/persediaan_barang", "root", "");
            java.sql.Statement stm=conn.createStatement();
            java.sql.ResultSet res=stm.executeQuery(sql);
            while(res.next()){
                model.addRow(new Object[]{res.getString(1),res.getString(2),res.getString(3),res.getString(4)});
                txtkodesupplier.setText(res.getString("kode_supplier"));
                txtnamasupplier.setText(res.getString("nama_supplier"));
                txtalamatsupplier.setText(res.getString("alamat_supplier"));
                txtnotelp.setText(res.getString("no_telp"));
            }
            tbsupplier.setModel(model);
        } catch (SQLException ex) {
        }
    }//GEN-LAST:event_btncariActionPerformed

