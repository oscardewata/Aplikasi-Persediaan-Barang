    public void getData( ){
     try{
      
           Statement stat = (Statement) koneksi.getKoneksi( ).createStatement( );
           String sql        = "select * from tbl_barang_keluar";
           ResultSet res   = stat.executeQuery(sql);

           while(res.next ()){
                Object[ ] obj = new Object[7];
                obj[0] = res.getString("no_keluar"); 
                obj[1] = res.getString("kode_barang");
                obj[2] = res.getString("nama_barang"); 
                obj[3] = res.getString("stok_barang");
                obj[4] = res.getString("jumlah"); 
                obj[5] = res.getString("sisa_barang");
                obj[6] = res.getString("tanggal");
                
                model.addRow(obj);
            }
      }catch(SQLException err){
            JOptionPane.showMessageDialog(null, err.getMessage() );
      }
}
