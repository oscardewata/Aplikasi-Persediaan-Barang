    private void simpantransaksiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpantransaksiActionPerformed
        if(no_masuk.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Nomor Masuk Tidak Boleh Kosong");
        }else if(cmbnomorfaktur.getSelectedItem().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Nomor Faktur Tidak Boleh Kosong");
        }

        if (!no_masuk.getText().equals("") && !cmbnomorfaktur.getSelectedItem().equals("")){
            try {
                Connection koneksi = DriverManager.getConnection("jdbc:mysql://localhost:3306/persediaan_barang","root","");
                koneksi.createStatement().executeUpdate("INSERT INTO tbl_barang_masuk (no_masuk, no_faktur, nama_supplier, nama_barang, jumlah, tanggal) VALUES ('" + no_masuk.getText() + "','" + cmbnomorfaktur.getSelectedItem()+ "', '" + nama_supplier.getText() + "', '" + nama_barang.getText() + "', '" + jumlah.getText() + "', '" + tanggalbarangmasuk.getText() + "')");
                reset();
                JOptionPane.showMessageDialog(null, "Input Barang Masuk Berhasil");
            } catch (SQLException ex) {
                Logger.getLogger(barang_keluar.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_simpantransaksiActionPerformed

