    public void tampil_combo()
    {
        try {
        Connection con = koneksi.getKoneksi();
        Statement stt = con.createStatement();
        String sql = "select no_faktur from tbl_transaksi_pembelian order by no_faktur asc";      
        ResultSet res = stt.executeQuery(sql);                               
        cmbnomorfaktur.addItem("==Pilih==");
        while(res.next()){
            Object[] ob = new Object[3];
            ob[0] = res.getString(1);
            
            
            cmbnomorfaktur.addItem((String) ob[0]);                                      
        }
        res.close(); stt.close();
         
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }
    
    public void tampil()
    {
        try {
        Connection con = koneksi.getKoneksi();
        Statement stt = con.createStatement();
        String sql = "select nama_supplier, nama_barang, jumlah from tbl_transaksi_pembelian where no_faktur='"+cmbnomorfaktur.getSelectedItem()+"'";  
        ResultSet res = stt.executeQuery(sql);
        
        while(res.next()){
            Object[] ob = new Object[3];
            ob[0]=  res.getString(1);
            ob[1]=  res.getString(2);
            ob[2]=  res.getString(3);
            
            nama_supplier.setText((String) ob[0]);
            nama_barang.setText((String) ob[1]);
            jumlah.setText((String) ob[2]);
        }
        res.close(); stt.close();
         
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }              
    }
    
